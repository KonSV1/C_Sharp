void PrintArrey(int[,] matr)        // метод вывода на экран массива
{
    for (int i = 0; i < matr.GetLength(0); i ++)     
    {
        for (int j = 0; j < matr.GetLength(1); j++) 
        {
            if(matr[i,j] == 0) Console.Write($" ");
            else Console.Write($"+");
            //Console.Write($"{matr[i, j]} ");
        }
        Console.WriteLine();
    }
}

void FillImage(int x, int y)        // метод закрашивания 
{
    if(pic[x, y] == 0)
    {
        pic[x, y] = 1;
        FillImage(x-1,y);
        FillImage(x,y-1);
        FillImage(x+1,y);
        FillImage(x,y+1);
    }
}

